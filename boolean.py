is_online = True  # A boolean variable representing online status
is_logged = False  # A boolean variable representing logged status

# Print the boolean values
print(is_online)  # Output: True
print(is_logged)  # Output: False

# Invert the value of 'is_logged' using the 'not' operator
is_logged_in = not is_logged  # This will make is_logged_in True because is_logged is False
print(is_logged_in)  # Output: True
